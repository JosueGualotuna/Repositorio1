    public void cargarTabla(){
    
        DefaultTableModel modelo = new DefaultTableModel();
        tablaPersona.getModel();
        modelo.setRowCount(0);
    
    
        ArrayList<persona> personas = registroPantalla.getListaPersona();
    for(persona p : personas){
    
modelo.addRow(new Object[]{p.getNombre(),p.getCedula()});

    }

    }

    public void agregaraLaTabla(){
        String nombre = txtNombre.getText();
        int cedula = Integer.parseInt(txtCedula.getText());
registroPantalla.agregar(new persona(nombre, cedula));
    }
    
    
