private void cargarDatosEnTabla() {
    DefaultTableModel modelo = (DefaultTableModel) TablaEliminar.getModel();
    modelo.setRowCount(0); // Limpia la tabla antes de cargar nuevos datos

    for (Datos dato : Datos.listaDatos) {
        String[] fila = {
            dato.getNombre(),
            dato.getApellido(),
            String.valueOf(dato.getCedula()),
            String.valueOf(dato.getEdad())
        };
        modelo.addRow(fila);
    }
}

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = TablaEliminar.getSelectedRow();

    if (filaSeleccionada != -1) {
        // Obtener la c√©dula del registro seleccionado
        int cedula = Integer.parseInt(TablaEliminar.getValueAt(filaSeleccionada, 2).toString());

        // Intentar eliminar el dato
        if (Datos.eliminarDato(cedula)) {
            JOptionPane.showMessageDialog(this, "Dato eliminado correctamente.");
            cargarDatosEnTabla(); // Refresca la tabla
        } else {
            JOptionPane.showMessageDialog(this, "Error al eliminar el dato.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Por favor, seleccione un dato de la tabla.");
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

