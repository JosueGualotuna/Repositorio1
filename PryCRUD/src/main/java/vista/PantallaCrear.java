    private void btnCrearSubActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearSubActionPerformed

        if(txtNombre.getText().equals("")||txtApellidos.getText().equals("")||txtCedula.getText().equals("")||txtEdad.getText().equals("")){
JOptionPane.showMessageDialog(this, "POR FAVOR INGRESE TODOS LOS DATOS");
}else{
Datos RecopilarDato = new Datos();
        RecopilarDato.setNombre(txtNombre.getText());
        RecopilarDato.setApellido(txtApellidos.getText());
        RecopilarDato.setCedula(Integer.parseInt(txtCedula.getText()));
        RecopilarDato.setEdad(Integer.parseInt(txtEdad.getText()));
                Datos.listaDatos.add(RecopilarDato);

JOptionPane.showMessageDialog(this, "DATOS CREADOS EXITOSAMENTE");
        txtNombre.setText("");
        txtApellidos.setText("");
        txtCedula.setText("");
        txtEdad.setText("");

}
        
    }//GEN-LAST:event_btnCrearSubActionPerformed

